package javaPrograms;

public class JP14 {

	public static void main(String[] args) {
		// find the sum of first 100 numbers
		
		int sum = 0;
		
		for(int i=1; i<=100; i++)
		{
			sum = sum + i ;  //0+1=1 1+1=2 2+2=4 4+3=7 7+4=11 11+5=16
		
		}
		System.out.println("sum of 1st 100 numbers is :" +sum);

	}

}
