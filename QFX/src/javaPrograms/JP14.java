package javaPrograms;

public class JP14 {

	public static void main(String[] args) {
		// find the sum of first 100 numbers
		
		int sum = 0;
		
		for(int i=1; i<=100; i++)
		{
			sum = sum + i ;  //0+1=1 1+2=3 3+3=6 6+4=10 10+5=15 ...& so on
		
		}
		System.out.println("sum of 1st 100 numbers is :" +sum);

	}

}
