package operatorsinjava;

public class RelationalOperator {

	public static void main(String[] args) {
		int x=23, y=25;
		boolean z;
		
		//less than operator
		z=x<y;
		System.out.println("x less than y  :" +z);
		
		
		//greater than operator
		z=x>y;
		System.out.println("x greater than y  :" +z);
		
		//less than equal to
		z=x<=y;
		System.out.println("x less than equal y  :" +z);
		
		//greater than equal to 
		z=x>=y;
		System.out.println("x greater than equal y  :" +z);
		
		//equal to
		z=x==y;
		System.out.println("x equal to y  :" +z);
		
		//not equal to
		
		z=x!=y;
		System.out.println("x less than y  :" +z);

	}

}
